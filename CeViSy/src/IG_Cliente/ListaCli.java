    public ListaCli() {
        try{
        initComponents();
        setLocationRelativeTo(null);
        setResizable(false);
        setDefaultCloseOperation(1);
        String[] columnas = {"Nombre","Apellido","Domicilio","Telefono","CUIL","Id_Cliente","Fecha_Inscripcion"};
        modelo.setColumnIdentifiers(columnas);

        GestorClientes gc = new GestorClientes();
        Collection asociados = gc.listarCliente();
        
        ArrayList clientes = (ArrayList) asociados;
        
        Iterator<Cliente> it = clientes.iterator();
        
        while(it.hasNext()){
                Cliente cli = it.next();
                
                String nombre = cli.getNombre();
                String apellido = cli.getApellido();
                String domicilio = cli.getDomicilio();
                Integer telefono = cli.getTelefono();
                String cuil = cli.getCuil();
                Integer id = cli.getIdCliente();
                Date fech_ins = cli.getFechaInscripcion();
                
            Object[] fila = {nombre,apellido,domicilio,telefono,cuil,id,fech_ins};
            modelo.addRow(fila);
        }
        this.jTable1.setModel(modelo);
        this.jScrollPane1.setRowHeader(null);
        }catch(DateAccessException exc) {
            System.out.println(exc);
        }
    }

