    public Principal() {
        try{
        initComponents();
        setLocationRelativeTo(null);
        setResizable(false);
        setDefaultCloseOperation(0);
        
        
        //Funcion automatica qe verifica si hay Cliente con
        //fecha de devolucion vencidas, de sewr asi,
        // se ejecuta lo siguiente aqui detallado...
        GestorAlquileres ga = new GestorAlquileres();
        GestorMultas gm = new GestorMultas();
        
        Collection alquiler = ga.listaalquileres();
        
        ArrayList ver = (ArrayList) alquiler;       
        Iterator<Alquileres> it = ver.iterator();
        
        //Buscar cliente con fecha de entrega vencida
        while(it.hasNext()){
            Alquileres alq = it.next();
            
            java.sql.Date fe = alq.getFecha_entrega();
            Integer idCli = alq.getId();
            Integer est = alq.getEstado();
            Integer idAlq = alq.getId_alquiler();
            Integer mon = alq.getMonto_alquiler();
            
            CeViSy v = new CeViSy();
            java.sql.Date venc = v.calcfecha();
            
            //Si se cumple la condicion , se carga al cliente en la 
            //DB de Multas
            if(fe.before(venc) && est.equals(1)){
                Integer id_multa;
            //Se genera el id_multa
                int i=0;
                do{
                Random rn = new Random();
                Integer id = rn.nextInt(9000)+1000;
                Multa nuevo = gm.buscarmultas(id);

                if(nuevo!=null){
                    i++;
                }id_multa = id;
                    i=0;
                }while(i!=0);

                    Integer estado = 2;
                    Double mnt = mon.doubleValue();
                    Multa multa = new Multa(id_multa, mnt, fe, estado, idCli, idAlq);
                    gm.agregarMulta(multa);
                    
            //Modificacion en DB de Alquileres ya que el cliente
            //ahora posee una multa
            Alquileres cambiar = ga.buscaralquiler(idAlq);
            java.sql.Date fec_a = cambiar.getFecha_alquiler();
                        
            Alquileres corregido = new Alquileres(idAlq,fec_a,mon,fe,estado,idCli);
            ga.devolucionoMulta(corregido);
                        }
            
        }
            
        }catch(DateAccessException e){
            System.out.println(e);
        }        
    }

