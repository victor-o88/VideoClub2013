    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(!jTextField1.getText().equals("")){
        try{
            GestorClientes gc = new GestorClientes();
            String id = jTextField1.getText();
            Integer idcli = Integer.parseInt(id);            
            Cliente existe = gc.buscarCliente(idcli);
            Integer id_cli=0, est=0, ban=0;

            if(existe!=null){

                GestorAlquileres ga = new GestorAlquileres();
                Collection estan = ga.listaalquileres();

                ArrayList castear = (ArrayList)estan;
                Iterator<Alquileres> it = castear.iterator();

                while(it.hasNext()){
                    Alquileres alq = it.next();

                    id_cli = alq.getId();
                    est = alq.getEstado();
                    
                    if(id_cli.equals(idcli) && est.equals(1)){
                        ban=1;
                       }

                }
                if(ban==1){
                    
                    JOptionPane.showMessageDialog(null,"Cliente con Alquiler realizado");

                }
                else{

                    GestorMultas gm = new GestorMultas();
                    Collection ver = gm.listamultas();

                    ArrayList casteartwo = (ArrayList)ver;
                    Iterator<Multa> ot = casteartwo.iterator();
                    while(ot.hasNext() && idcli!=id_cli){
                        Multa m = ot.next();

                        id_cli = m.getIdC();
                        est = m.getEstado();
                        
                        if(id_cli.equals(idcli) && est.equals(2)){
                            ban=1;
                            }

                    }
                    if(ban==1){

                        JOptionPane.showMessageDialog(null,"Cliente con multa pendiente");

                    }else{

                        RealizarAlquiler ra = new RealizarAlquiler(idcli);
                        ra.setVisible(true);
                        dispose();

                    }
                }
            }else {
                JOptionPane.showMessageDialog(null,"El Cliente, No existe");
            }
            
        }catch(DateAccessException e){
            System.out.println(e);
        }
        }else
                if(jTextField1.getText().equals("")){
                JOptionPane.showMessageDialog(null,"Ingrese un Id de Cliente");
                }
    }//GEN-LAST:event_jButton2ActionPerformed

